def mimateba(num1, num2):
    return num1 + num2

print(mimateba(14, 7))

def gamokleba(num1, num2):
    return num1 - num2

print(gamokleba(14, 7))

def gamravleba(num1, num2):
    return num1 * num2

print(gamravleba(14, 7))

def gayofa(num1, num2):
    return num1 / num2

print(gayofa(14, 7))